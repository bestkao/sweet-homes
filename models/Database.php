<?php

/**
 * This is an interface Database which provides the minimal yet major functionality 
 * to use a Database system -- adding and removing of data.
 * This can be implemented by anyone interested in using a Database.
 * But, the user of this interface needs to at least implement the adding and removing
 * of data from a database.
 * Implementing class is ModelDatabase.
 * @author sashi
 */
interface Database 
{
    /**
     * Adds the given data to the database.
     * @param type $data
     */
    public function add($data);
    
    /**
     * Removes the given data from the database.
     * @param type $data
     */
    public function remove($data);
}
